def client():
    with open('./apps/order/app_private_key.pem', 'r') as f:
        content = f.read()
    app_private_key_string = content
    with open('./apps/order/alipay_public_key.pem', 'r') as f:
        content = f.read()
    alipay_public_key_string = content

    """
    设置配置，包括支付宝网关地址、app_id、应用私钥、支付宝公钥等，其他配置值可以查看AlipayClientConfig的定义。
    """
    alipay_client_config = AlipayClientConfig()
    alipay_client_config.server_url = 'https://openapi.alipaydev.com/gateway.do'
    alipay_client_config.app_id = '2016102700772307'
    alipay_client_config.app_private_key = app_private_key_string
    alipay_client_config.alipay_public_key = alipay_public_key_string
    alipay_client_config.sign_type = 'RSA2'
    alipay_client_config.charset = 'UTF-8'

    """
    得到客户端对象。
    注意，一个alipay_client_config对象对应一个DefaultAlipayClient，定义DefaultAlipayClient对象后，
    alipay_client_config不得修改，如果想使用不同的配置，请定义不同的DefaultAlipayClient。
    logger参数用于打印日志，不传则不打印，建议传递。
    """
    return DefaultAlipayClient(alipay_client_config=alipay_client_config)
